// Set column widths similar to original
ws['!cols'] = [
  { wch: 22 }, { wch: 8 },
  { wch: 16 }, { wch: 12 }, { wch: 12 }, { wch: 12 }, { wch: 12 }, { wch: 12 },
  { wch: 20 }, { wch: 12 }, { wch: 12 },
];

// Build merges
const merges = [];
rows.forEach((r, i) => {
  if (r.type === 'section' || r.type === 'instruction') {
    merges.push({ s: { r: i, c: 0 }, e: { r: i, c: 10 } });
  } else if (r.type === 'sub') {
    merges.push({ s: { r: i, c: 0 }, e: { r: i, c: 10 } });
  } else if (r.type === 'label') {
    merges.push({ s: { r: i, c: 0 }, e: { r: i, c: 1 } });
    merges.push({ s: { r: i, c: 2 }, e: { r: i, c: 7 } });
    merges.push({ s: { r: i, c: 8 }, e: { r: i, c: 10 } });
  } else if (r.type === 'content') {
    merges.push({ s: { r: i, c: 2 }, e: { r: i, c: 7 } });
    merges.push({ s: { r: i, c: 8 }, e: { r: i, c: 10 } });
  }
});
ws['!merges'] = merges;

// Row heights
const rh = rows.map(r => {
  const maxLines = Math.max(...r.data.map(c => String(c).split('\n').length));
  return { hpt: Math.max(20, Math.min(maxLines * 14, 400)) };
});
ws['!rows'] = rh;

XLSX.utils.book_append_sheet(wb, ws, 'Квалификатор');


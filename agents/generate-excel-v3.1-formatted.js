// helpers
// label row: A:B merged, C:H merged, I:K merged
function labelRow(label, content, comment) {
  return { type: 'label', data: [label, '', content, '', '', '', '', '', comment || '', '', ''] };
}
// section header: full row A:K merged
function sectionHeader(text) {
  return { type: 'section', data: [text, '', '', '', '', '', '', '', '', '', ''] };
}
// sub-header: A:B merged, rest empty
function subHeader(text) {
  return { type: 'sub', data: [text, '', '', '', '', '', '', '', '', '', ''] };
}
// content-only: C:H merged, I:K merged (no A:B label)
function contentRow(content, comment) {
  return { type: 'content', data: ['', '', content, '', '', '', '', '', comment || '', '', ''] };
}
// full instruction row: A:K merged
function instructionRow(text) {
  return { type: 'instruction', data: [text, '', '', '', '', '', '', '', '', '', ''] };
}
// header row with "Вопросы этапа" | "Комментарии"
function headerRow(left, right) {
  return { type: 'label', data: ['', '', left, '', '', '', '', '', right, '', ''] };
}


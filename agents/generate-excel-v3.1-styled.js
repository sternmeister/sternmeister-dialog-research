// ALIGNMENT
// ══════════════════════════════════════
const A_CENTER = { vertical: 'middle', horizontal: 'center', wrapText: true };
const A_WRAP   = { vertical: 'middle', wrapText: true };

// ══════════════════════════════════════
// COLUMNS (11 columns like original)
// ══════════════════════════════════════

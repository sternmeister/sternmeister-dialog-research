const BLUE_FOMO   = 'FFA4C2F4'; // FOMO header
const GREEN       = 'FFD9EAD3'; // content / questions
const YELLOW      = 'FFFFE599'; // comments / instructions
const YELLOW_FOMO = 'FFFFF2CC'; // FOMO sub-headers

const fillOf = argb => ({ type: 'pattern', pattern: 'solid', fgColor: { argb } });

// ══════════════════════════════════════
// FONTS (from original)
// ══════════════════════════════════════
const F_LABEL    = { name: 'Arial', size: 12, bold: true,  color: { theme: 1 } };

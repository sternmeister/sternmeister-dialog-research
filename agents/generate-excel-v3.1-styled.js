// ══════════════════════════════════════
// BUILDER FUNCTIONS
// ══════════════════════════════════════
let rowNum = 0;

function addSectionHeader(text, fontSize) {
  rowNum++;
  const row = ws.getRow(rowNum);
  ws.mergeCells(rowNum, 1, rowNum, 11);
  const cell = row.getCell(1);
  cell.value = text;
  cell.font = fontSize ? { ...F_LABEL, size: fontSize } : F_LABEL;
  cell.fill = fillOf(BLUE_DARK);
  cell.alignment = A_CENTER;
  row.height = Math.max(30, text.split('\n').length * 18);
}

function addSubHeader(text) {
  rowNum++;
  const row = ws.getRow(rowNum);
  ws.mergeCells(rowNum, 1, rowNum, 11);
  const cell = row.getCell(1);
  cell.value = text;
  cell.font = F_LABEL;
  cell.fill = fillOf(BLUE_LIGHT);
  cell.alignment = A_CENTER;
  row.height = Math.max(25, text.split('\n').length * 18);
}

function addInstruction(text) {
  rowNum++;
  const row = ws.getRow(rowNum);
  ws.mergeCells(rowNum, 1, rowNum, 11);
  const cell = row.getCell(1);
  cell.value = text;
  cell.font = F_INSTR;
  cell.fill = fillOf(YELLOW);
  cell.alignment = A_CENTER;
  row.height = Math.max(25, text.split('\n').length * 16);
}

function addLabelRow(label, content, comment) {
  rowNum++;
  const row = ws.getRow(rowNum);

  // A:B merged — label
  ws.mergeCells(rowNum, 1, rowNum, 2);
  const c1 = row.getCell(1);
  c1.value = label;
  c1.font = F_LABEL;
  c1.fill = fillOf(label.startsWith('*') || label.startsWith('Информация') ? YELLOW : BLUE_DARK);
  c1.alignment = A_CENTER;

  // C:H merged — content
  ws.mergeCells(rowNum, 3, rowNum, 8);
  const c3 = row.getCell(3);
  c3.value = content;
  c3.font = F_CONTENT;
  c3.fill = fillOf(GREEN);
  c3.alignment = A_WRAP;

  // I:K merged — comment
  ws.mergeCells(rowNum, 9, rowNum, 11);
  const c9 = row.getCell(9);
  c9.value = comment || '';
  c9.font = F_COMMENT;
  c9.fill = fillOf(YELLOW);
  c9.alignment = A_WRAP;

  const maxLines = Math.max(
    (label || '').split('\n').length,
    (content || '').split('\n').length,
    (comment || '').split('\n').length
  );
  row.height = Math.max(28, Math.min(maxLines * 16, 400));
}

